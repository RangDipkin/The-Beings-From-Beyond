package lighting;

/**
 *
 * @author Travis
 */
public class PreciseCoordinate {
    double x;
    double y;
    
    PreciseCoordinate(double x, double y){
        this.x = x;
        this.y = y;
    }
    
    double getX() {
        return x;
    }
    
    double getY() {
        return y;
    }
}
